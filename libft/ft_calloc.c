#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char		*dst;
	size_t		total;
	size_t		i;

	total = nmemb * size;
	if (!(dst = malloc(total)))
		return (NULL);
	i = 0;
	while (total--)
	{
		dst[i] = 0;
		i++;
	}
	return ((void *)dst);
}
